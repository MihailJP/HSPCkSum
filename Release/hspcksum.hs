%index
md5sum
バッファ内のデータのMD5を取得する

%prm
(p1,p2)
p1=変数 : 対象となるバッファ
p2=0〜 : バッファのサイズ

%inst
バッファ p1 の内容の MD5 ハッシュを返します。バッファのサイズは、p2 で指定します。
varmd5と違って、関数として記述します。
MD5とはハッシュ関数の一つで、与えられた入力に対して128ビットのハッシュ値を出力します。
返り値は文字列型で、「900150983cd24fb0d6963f7d28e17f72」のような32桁の16進数で返されます。

なお、 MD5 ハッシュの強衝突耐性は既に破れており、安全とはいえない状態となっています。
互換のために実装していますが、SHA-256以上が推奨されます。

