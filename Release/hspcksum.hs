%index
sha512sum
バッファ内のデータのSHA-512を取得する

%prm
(p1,p2)
p1=変数 : 対象となるバッファ
p2=0〜 : バッファのサイズ

%inst
バッファ p1 の内容の SHA-512 ハッシュを返します。バッファのサイズは、p2 で指定します。
SHA-512とはSHA-2と呼ばれるハッシュ関数の一つで、与えられた入力に対して512ビットのハッシュ値を出力します。
返り値は文字列型で、「ddaf35a193617abacc417349ae20413112e6fa4e89a97ea20a9eeee64b55d39a2192992a274fc1a836ba3c23a3feebbd454d4423643ce80e2a9ac94fa54ca49f」のような128桁の16進数で返されます。

MD5 や SHA-1 には脆弱性が見つかっているため、現在ハッシュ関数としては SHA-256 以上が推奨されています。
SHA-512 の計算方法は SHA-256 と大枠は同じですが、64ビット整数で計算すること、ブロックサイズが異なること、ループ回数が64回でなく80回であること、ビットシフトやビット回転の桁数が異なることなどの差異があります。

